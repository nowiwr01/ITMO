vector<uint64_t> multiply(const vector<uint64_t> &first, const vector<uint64_t> &second) {
    vector<uint64_t> result(200, 0);
    for (int i = 0; i < 200; i++) {
        for (int j = 0; j <= i; j++) {
            result[i] += first[j] * second[i - j] % MODULE;
            result[i] %= MODULE;
        }
    }
    return result;
}

